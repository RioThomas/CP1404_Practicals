import random
print(random.randint(5, 20))        # line 1            - an int between 5 and 20 [inclusive]
print(random.randrange(3, 10, 2))   # line 2            - an int between 3 and 9 [inclusive] and only odds (no 4)
print(random.uniform(2.5, 5.5))     # line 3            - a float between 2.5 and 5.5 [inclusive]

print(random.randint(1, 100))       # line 4            - an int between 1 and 100 [inclusive]
